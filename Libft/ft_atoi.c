#include "libft.h"

int		ft_atoi(const char *str)
{
	short	sign;
	long	n;

	n = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	sign = (*str == '-' || *str == '+') ? (44 - *str++) : (1);
	while (*str > 47 && *str < 58)
		n = n * 10 + *str++ - 48;
	return (sign * (int)n);
}
