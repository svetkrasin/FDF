#include "libft.h"
#include <stddef.h>

void	ft_bzero(void *s, size_t n)
{
	while (n)
	{
		if (n >= 8)
		{
			*(long *)s &= 0x00;
			s += 8;
			n -= 8;
		}
		else if (n >= 4)
		{
			*(int *)s &= 0x00;
			s += 4;
			n -= 4;
		}
		else
		{
			*(char *)s &= 0x00;
			s += 1;
			n -= 1;
		}
	}
}
